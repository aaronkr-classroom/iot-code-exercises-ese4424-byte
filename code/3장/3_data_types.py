# 3_data_types.py
a = 10        # 정수형 (int)
b = 3.14      # 상수형 (float)
c = "hello"   # 문자열 (str)
d = 'goodbye' # 문자열 (str)
e = True      # 불리언(bool)

print(f"{a}: {type(a)}") # f: fomat
print(f"{b}: {type(b)}")
print(f"{c}: {type(c)}")
print(f"{d}: {type(d)}")
print(f"{e}: {type(e)}")

print(a, ":" , type(a))